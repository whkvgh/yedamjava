package Bean;

public class InOutBean {
	private String sub_group;
	private String i_name;
	private int in_count;
	private int out_count;
	private String date_list;
	private String store_code;
	private String bigo;
	public String getSub_group() {
		return sub_group;
	}
	public void setSub_group(String sub_group) {
		this.sub_group = sub_group;
	}
	public String getI_name() {
		return i_name;
	}
	public void setI_name(String i_name) {
		this.i_name = i_name;
	}
	public int getIn_count() {
		return in_count;
	}
	public void setIn_count(int in_count) {
		this.in_count = in_count;
	}
	public int getOut_count() {
		return out_count;
	}
	public void setOut_count(int out_count) {
		this.out_count = out_count;
	}
	public String getDate_list() {
		return date_list;
	}
	public void setDate_list(String date_list) {
		this.date_list = date_list;
	}
	public String getStore_code() {
		return store_code;
	}
	public void setStore_code(String store_code) {
		this.store_code = store_code;
	}
	public String getBigo() {
		return bigo;
	}
	public void setBigo(String bigo) {
		this.bigo = bigo;
	}
	
	
}
