package Bean;

public class ItemListBean {
	private String i_group;
	private String sub_group;
	private String i_name;
	private String i_size;
	private int unit;
	private String store_code;
	private int price;
	
	public String getI_group() {
		return i_group;
	}
	public void setI_group(String i_group) {
		this.i_group = i_group;
	}
	public String getSub_group() {
		return sub_group;
	}
	public void setSub_group(String sub_group) {
		this.sub_group = sub_group;
	}
	public String getI_name() {
		return i_name;
	}
	public void setI_name(String i_name) {
		this.i_name = i_name;
	}
	public String getI_size() {
		return i_size;
	}
	public void setI_size(String i_size) {
		this.i_size = i_size;
	}
	public int getUnit() {
		return unit;
	}
	public void setUnit(int unit) {
		this.unit = unit;
	}
	public String getStore_code() {
		return store_code;
	}
	public void setStore_code(String store_code) {
		this.store_code = store_code;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
