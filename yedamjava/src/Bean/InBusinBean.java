package Bean;

public class InBusinBean {
	private String busin_code;
	private String busin_name;
	private String busin_addr;
	private String busin_num;
	private String ceo;
	public String getBusin_code() {
		return busin_code;
	}
	public void setBusin_code(String busin_code) {
		this.busin_code = busin_code;
	}
	public String getBusin_name() {
		return busin_name;
	}
	public void setBusin_name(String busin_name) {
		this.busin_name = busin_name;
	}
	public String getBusin_addr() {
		return busin_addr;
	}
	public void setBusin_addr(String busin_addr) {
		this.busin_addr = busin_addr;
	}
	public String getBusin_num() {
		return busin_num;
	}
	public void setBusin_num(String busin_num) {
		this.busin_num = busin_num;
	}
	public String getCeo() {
		return ceo;
	}
	public void setCeo(String ceo) {
		this.ceo = ceo;
	}
	
}
